void Activity::Start (const uint16_t v_actionId)
{
    for (auto & widget : widgets)
    {
        widget.get().Draw ();
    }
}

void Activity::Update (const uint16_t v_actionId)
{
    for (auto & widget : widgets)
    {
        if (widget.get().IsUpdatable == true)
        {
            widget.get().Draw ();
        }
    }
}

