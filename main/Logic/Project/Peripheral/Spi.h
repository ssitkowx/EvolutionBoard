class Spi
{
    public:
        enum class EFlag : uint8_t
        {
            eDio        = 0,
            eQio        = 1,
            eRxData     = 2,
            eTxData     = 3,
            eDioQioAddr = 4,
            eCmd        = 5,
            eAddr       = 6,
            eDummy      = 7,
            eCd         = 7
        };

        Spi          () = default;
        virtual ~Spi () = default;

        virtual void      Send16Bits (const uint16_t * v_data, const uint16_t v_len) = 0;
        virtual uint8_t * Send       (const uint8_t  * v_data, const uint16_t v_len) = 0;
};

