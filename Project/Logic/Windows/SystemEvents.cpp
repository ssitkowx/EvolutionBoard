void SystemEvents::Add (uint16_t v_eventId)
{
    queue.push (v_eventId);
}

bool SystemEvents::IsEmpty (void)
{
    return queue.empty ();
}

uint16_t SystemEvents::Remove (void)
{
    uint16_t eventId = queue.front ();
    queue.pop ();
    return eventId;
}

